    private void txtKodeFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_txtKodeFocusLost
        ModelBarang brg = new ModelBarang();
        brg.setKode(txtKode.getText());
        brg.tampil();
        if (brg.getFlag() == 4) {
            txtNama.setText(brg.getNama());
            txtSatuan.setText(brg.getSatuan());
            txtHarga.setText("" + brg.getHarga());
            txtStok.setText("" + brg.getStok());
            txtNama.requestFocus();
            btnSimpan.setEnabled(false);
            btnUbah.setEnabled(true);
            btnHapus.setEnabled(true);
        } else {
            txtNama.setText("");
            txtSatuan.setText("");
            txtHarga.setText("");
            txtStok.setText("");
            txtNama.requestFocus();
            btnSimpan.setEnabled(true);
            btnUbah.setEnabled(false);
            btnHapus.setEnabled(false);
        }
    }//GEN-LAST:event_txtKodeFocusLost


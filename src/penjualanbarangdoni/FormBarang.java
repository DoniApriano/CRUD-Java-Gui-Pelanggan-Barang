    public void kosong() {
        ModelBarang brg = new ModelBarang();
        txtKode.setText("");
        txtKode.setEnabled(false);
        txtNama.setText("");
        txtSatuan.setText("");
        txtHarga.setText("");
        txtStok.setText("");
        txtKode.requestFocus();
        btnSimpan.setEnabled(true);
        btnUbah.setEnabled(false);
        btnHapus.setEnabled(false);
        brg.autoCode();
        txtKode.setText(brg.getKode());
    }

    public void tampilData() {
        String kode, nama, satuan, harga, stok;
        int baris = tabMode.getRowCount();
        for (int i = 0; i < baris; i++) {
            tabMode.removeRow(0);
        }

        try {
            Conn conn = new Conn();
            Connection connection = conn.openConnection();
            Statement statement = connection.createStatement();
            String sql = "SELECT * FROM barang ORDER BY kode_brg ASC";
            ResultSet resultSet = statement.executeQuery(sql);
            while (resultSet.next()) {
                kode = resultSet.getString("kode_brg");
                nama = resultSet.getString("nama_brg");
                satuan = resultSet.getString("satuan");
                harga = resultSet.getString("harga_brg");
                stok = resultSet.getString("stok");

                String[] data = {
                    kode, nama, satuan, harga, stok
                };
                tabMode.addRow(data);
            }
            resultSet.close();
            statement.close();
        } catch (SQLException e) {
            System.err.println(e);
        }
    }
